package railway;

public class BerthInfo {
    private int upper;
    private int lower;
    private int middle;
    private int sideUpper;

    public int getUpper() {
        return upper;
    }

    public void setUpper(int upper) {
        this.upper = upper;
    }

    public int getLower() {
        return lower;
    }

    public void setLower(int lower) {
        this.lower = lower;
    }

    public int getMiddle() {
        return middle;
    }

    public void setMiddle(int middle) {
        this.middle = middle;
    }

    public int getSideUpper() {
        return sideUpper;
    }

    public void setSideUpper(int sideUpper) {
        this.sideUpper = sideUpper;
    }

}
